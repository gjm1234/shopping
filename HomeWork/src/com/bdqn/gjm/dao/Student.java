package com.bdqn.gjm.dao;

public class Student {

	private int stuid;
	private String stuame;
	private String stusex;
	private String email;
	public int getStuid() {
		return stuid;
	}
	public void setStuid(int stuid) {
		this.stuid = stuid;
	}
	public String getStuame() {
		return stuame;
	}
	public void setStuame(String stuame) {
		this.stuame = stuame;
	}
	public String getStusex() {
		return stusex;
	}
	public void setStusex(String stusex) {
	this.stusex = stusex;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
}
